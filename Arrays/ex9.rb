# Favorite Number (Part 2)

favorites = [['Dave', 7], ['Miranda', 3], ['Jason', 11]]

# Flatten and print this array.

favorites.flatten!
p favorites

# The .flatten method turns a set of subarrays into a single non-nested array.
# The p method allows us to view the array in the console, rather than printing
# out each individual element, which we could do with puts.